/**
 * Hello World関数
 * "Hello, World!"を返すシンプルな関数
 */

/**
 * Hello Worldメッセージを返す
 * @returns {string} "Hello, World!"
 */
export function helloWorld() {
  return "Hello, World!";
}

/**
 * Hello Worldメッセージをコンソールに出力
 */
export function printHelloWorld() {
  console.log("Hello, World!");
}

export default helloWorld;
